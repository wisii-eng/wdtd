package com.wisii.wisedoc.view.ui.ribbon.edit;


public class FormulaVar {

	private String varName;

	public FormulaVar() {
	}

	public FormulaVar(String varName) {
		if(varName.startsWith("#")){
			String substring = varName.substring(2,varName.length()-1);
			this.varName = substring;
		}else{
			this.varName = varName;
		}
	}

	public String getVarName() {
		return varName;
	}

	public void setVarName(String varName) {
		this.varName = varName;
	}

	@Override
	public String toString() {
		return "#{" + varName +"}";
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((varName == null) ? 0 : varName.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		FormulaVar other = (FormulaVar) obj;
		if (varName == null) {
			if (other.varName != null)
				return false;
		} else if (!varName.equals(other.varName))
			return false;
		return true;
	}

	
}
